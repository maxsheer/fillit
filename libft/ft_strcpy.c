#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int k;

	k = 0;
	while (src[k] != '\0')
	{
		dest[k] = src[k];
		k++;
	}
	dest[k] = src[k];
	return (dest);
}
